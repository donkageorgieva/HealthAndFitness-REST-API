package healt.fitness.api.model;

public class CalorieVariations {
public int superLowCalories;
public int lowCalories;
public int moderateCalories;
public int highCalories;
public CalorieVariations (int superLowCalories, int lowCalories, int moderateCalories, int highCalories) {
	this.superLowCalories = superLowCalories;
	this.lowCalories = lowCalories;
	this.moderateCalories = moderateCalories;
	this.highCalories = highCalories;
	
}
}
